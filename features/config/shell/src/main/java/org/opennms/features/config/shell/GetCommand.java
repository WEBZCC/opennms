package org.opennms.features.config.shell;

import java.util.Optional;

import org.apache.karaf.shell.api.action.Argument;
import org.apache.karaf.shell.api.action.Command;
import org.apache.karaf.shell.api.action.Completion;
import org.apache.karaf.shell.api.action.lifecycle.Service;

@Command(scope = "opennms", name = "cm-get", description = "Get the value of a key")
@Service
public class GetCommand extends ConfigCommandSupport {

    @Argument(required = true)
    @Completion(value = KeyCompleter.class)
    String key;

    @Override
    public Object execute() {
        final Optional<String> value = keyHandler.getValue(key);
        if (value.isPresent()) {
            System.out.printf("%s = %s\n", key, value.get());
        } else {
            System.out.printf("No value at key: %s\n", key);
        }
        return null;
    }
}